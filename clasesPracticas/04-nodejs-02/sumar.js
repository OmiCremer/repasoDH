// función llamada sumar
// debe recibir dos parámetros
function sumar(numeroA, numeroB){
    //sumar los 2 parámetros
    const resultado = numeroA + numeroB;
    //retornar las suma de los mismo
    return resultado;
} 



//la cual debe ser exportada al final del archivo
module.exports = sumar;

// el modulo exporta sumar

//module es una variable . en la key exports le estoy setenado la variable sumar